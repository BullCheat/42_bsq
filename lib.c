int		ft_atoi(char *s)
{
	int		n;
	int		sign;

	while (*s <= ' ')
		s++;
	sign = *s == '-' ? -1 : 1;
	if (*s == '-' || *s == '+')
		s++;
	n = 0;
	while (*s >= '0' && *s <= '9')
		n = n * 10 + *s++ - '0';
	return (n * sign);
}
